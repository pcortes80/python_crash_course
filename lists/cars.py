cars = ['bmw', 'audi', 'toyota', 'subaru']

print("Here is the original list:")
print(cars)

# sorted() does not affect the actua order of the list
print("\nHere is the sorted list:")
print(sorted(cars))

print("\nHere is the original list again:")
print(cars)

print("\nThe length of the list uses the function len()")
print(f"The lenght of the list 'cars' is {len(cars)}.")
