places = ['italy', 'canada', 'paris', 'jerusalem', 'turkey']
print("These are the places I'd like to visit:")
print(places)

# sorted() does not affect the actual order of the list
print("\nHere is the sorted list:")
print(sorted(places))

print("\nHere is the original list again:")
print(places)

print("\nThe reverse list:")
places.reverse()
print(places)

print("\nThe sorted list of places is:")
places.sort()
print(places)
